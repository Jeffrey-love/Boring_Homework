# 密钥扩展
# 循环左移一个字节
def R_change(x):
    return ((x << 8) | (x >> 24)) & 0xffffffff


# S盒变换
def S_Box_change(x):
    tmp = 0
    tmp = (tmp << 8) | S_Box[(x >> 28) & 0x0f][(x >> 24) & 0x0f]
    tmp = (tmp << 8) | S_Box[(x >> 20) & 0x0f][(x >> 16) & 0x0f]
    tmp = (tmp << 8) | S_Box[(x >> 12) & 0x0f][(x >> 8) & 0x0f]
    tmp = (tmp << 8) | S_Box[(x >> 4) & 0x0f][(x >> 0) & 0x0f]
    return tmp


# 轮密钥加变换: 将每一列与扩展密钥进行异或
def add_round_key(enc, ikey):
    round_enc = []
    for i in range(4):
        for j in range(4):
            round_enc.append((enc[4 * i + j] ^ ((ikey[j] << 8 * i) >> 24)) & 0xff)
    return bytes(round_enc)


# S盒变换: 前4位为行号，后4位为列号
def Subbytes(enc):
    round_enc = []
    for i in enc:
        round_enc.append(S_Box[i >> 4][i & 0x0f])
    return bytes(round_enc)


# 行变换: 按字节循环移位
def rows_exchange(enc):
    round_enc = b''
    round_enc += enc[0:4] + enc[5:8] + enc[4:5] + enc[10:12] + enc[8:10] + enc[15:16] + enc[12:15]
    return round_enc


# 有限域上的乘法 GF(2^8)
def GFMul(a, b):
    p, hi_bit_set = 0, 0
    for counter in range(8):
        if b & 1 != 0:
            p ^= a
        hi_bit_set = a & 0x80
        a = (a << 1) & 0xff
        if hi_bit_set != 0:
            a ^= 0x1b  # x^8 + x^4 + x^3 + x + 1
        b >>= 1
    return p & 0xff


# 列变换
def columns_exchange(enc):
    round_enc = [0] * 16
    for i in range(4):
        arr = []
        for j in range(4):
            arr.append(enc[i + j * 4])
        round_enc[i] = GFMul(0x02, arr[0]) ^ GFMul(0x03, arr[1]) ^ arr[2] ^ arr[3]
        round_enc[i + 4] = arr[0] ^ GFMul(0x02, arr[1]) ^ GFMul(0x03, arr[2]) ^ arr[3]
        round_enc[i + 8] = arr[0] ^ arr[1] ^ GFMul(0x02, arr[2]) ^ GFMul(0x03, arr[3])
        round_enc[i + 12] = GFMul(0x03, arr[0]) ^ arr[1] ^ arr[2] ^ GFMul(0x02, arr[3])
    return bytes(round_enc)


