# AES-128
class AES(object):
    def __init__(self, K: bytes):
        self.Nk, self.Nb, self.Nr = 4, 4, 10    # 四个32bit字, 四列, 10轮加密
        self.K = K[:self.Nk * 4]    # 取密钥，下面判断是否足够16比特
        while len(self.K) < self.Nk * 4:
            self.K += b'\x00'
        self.W = self.key_expansion()    # 对密钥进行扩展

    def aes_encrypt(self, message: bytes) -> bytes:
        # 明文不足16*8bits补零
        while len(message) % 16 != 0:
            message += b'\x00'
        c = b''
        for i in range(len(message) // 16):
            # 对每一个16*8bits的块进行循环
            enc = message[i * 16:i * 16 + 16]
            ikey = self.W[:4]
            enc = add_round_key(enc, ikey)      # 轮密钥变换
            for round1 in range(1, self.Nr):
                enc = Subbytes(enc)             # S盒替换
                enc = rows_exchange(enc)        # 行变换
                enc = columns_exchange(enc)     # 列变换
                ikey = self.W[4 * round1: 4 * round1 + 4]
                enc = add_round_key(enc, ikey)  # 轮密钥变换
            # 最后一轮不进行列变换
            enc = Subbytes(enc)             # S盒替换
            enc = rows_exchange(enc)        # 行变换
            ikey = self.W[4 * self.Nr:]
            enc = add_round_key(enc, ikey)  # 轮密钥变换
            c += enc
        return c

    def aes_decrypt(self, message: bytes) -> bytes:
        # 解密
        c = b''
        for i in range(len(message) // 16):
            enc = message[i * 16:i * 16 + 16]
            ikey = self.W[self.Nr * 4:]
            enc = add_round_key(enc, ikey)
            for round1 in range(self.Nr-1, 0, -1):
                enc = rows_inexchange(enc)  # 列逆变换
                enc = InvSubbytes(enc)      # S盒逆替换
                ikey = self.W[4 * round1:4 * round1 + 4]
                enc = add_round_key(enc, ikey)  # 轮密钥变换
                enc = columns_inexchange(enc)   # 列逆变换
            enc = rows_inexchange(enc)      # 行逆替换
            enc = InvSubbytes(enc)          # S盒逆替换
            ikey = self.W[:4]
            enc = add_round_key(enc, ikey)  # 轮密钥变换
            c += enc
        return c

    def key_expansion(self):
        # 密钥扩展函数，  4*4*8bits=128bits -> 4*(10+1)*32bits=1408bits
        w = []
        for i in range(self.Nk):
            tmp = 0
            for j in range(4):
                tmp = (tmp << 8) | self.K[4 * i + j]
            w.append(tmp)
        for i in range(self.Nk, self.Nb * (self.Nr + 1)):   # 密钥 K 扩展生成 Nb(Nr+1)个字
            tmp = w[i - 1]
            if i % self.Nk == 0:
                tmp = S_Box_change(R_change(tmp)) ^ Rcon[i // self.Nk - 1]
            elif self.Nk > 6 and i % self.Nk == 4:
                tmp = S_Box_change(tmp)
            w.append(w[i - self.Nk] ^ tmp)
        return w


